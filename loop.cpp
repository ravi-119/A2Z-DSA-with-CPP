// while loop

#include <iostream>
using namespace std;
int main ()
{
    cout<< "With the help of while loop" << endl;
    int a = 10;
    while( a < 20 )
    {
        cout << "value of a: " << a << endl;
        a++;
    } 
    return 0;
}

// For loop

#include <iostream>
using namespace std;
int main() {
    cout << " With the help of for loop" << endl;
    for( int a = 10; a < 20 ; a = a + 1)
    {
        cout<<" Value of a is : " << a << endl;
    }
    return 0;
}
